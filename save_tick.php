<?php

header('Content-Type: application/json');

$input = json_decode(
    file_get_contents('php://input'),
    true
);

/*
|--------------------------------------------------------------------------
| Validate Input
|--------------------------------------------------------------------------
*/

if(
    !isset($input['quote'])
){
    exit(
        json_encode([
            'success' => false,
            'error'   => 'missing quote'
        ])
    );
}

/*
|--------------------------------------------------------------------------
| Storage File
|--------------------------------------------------------------------------
*/

$file =
    __DIR__.'/storage/historical_ticks.json';

$ticks = [];

/*
|--------------------------------------------------------------------------
| Load Existing Data
|--------------------------------------------------------------------------
*/

if(file_exists($file)){

    $content =
        file_get_contents($file);

    $ticks =
        json_decode(
            $content,
            true
        );

    if(!is_array($ticks)){
        $ticks = [];
    }

}

/*
|--------------------------------------------------------------------------
| Append Tick
|--------------------------------------------------------------------------
*/

$ticks[] = [

    'quote' =>
        floatval(
            $input['quote']
        ),

    'epoch' =>
        intval(
            $input['epoch']
            ?? time()
        )

];

/*
|--------------------------------------------------------------------------
| Keep Last 10,000 Ticks
|--------------------------------------------------------------------------
*/

if(count($ticks) > 10000){

    $ticks = array_slice(
        $ticks,
        -10000
    );

}

/*
|--------------------------------------------------------------------------
| Save
|--------------------------------------------------------------------------
*/

file_put_contents(

    $file,

    json_encode(
        $ticks
    )

);

/*
|--------------------------------------------------------------------------
| Response
|--------------------------------------------------------------------------
*/

echo json_encode([

    'success' => true,

    'total' =>
        count($ticks)

]);
